package geometria;

/**
 * Zawiera podstawowe funkcje, które można wykonać na figurach
 * geometrycznych. Te metody to np. pole i obwód.
 *
 * @author Student
 */
public interface Obliczenia {
    /**
     * Funkcja która oblicza obwód figury. Coś dalej.
     * 
     * @return Obwód figury.
     */
    double Obwod();
    /**
     * Funkcja oblicza pole figury.
     * 
     * @return Pole figury.
     */
    double Pole();
}
